                    false;

            if (isOpenNavToggle || isChildOfExpanded || isExpanded) {
                return;
            }

            closeNav();

        }, false);

        if (openNavToggle) {
            openNavToggle.addEventListener('click', function(e) {
                e.preventDefault();
                document.getElementById('main-nav').classList.toggle(expandedClass);
                document.body.classList.add(noScrollClass);
                updateAccessibilityForSubnav(true, accessibleAccountLinks);
