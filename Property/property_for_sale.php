            if (typeof fn !== 'function') {
                return;
            }

            var lastCall = 0;
            delay = typeof delay === 'number' ? delay : 100;

            return function() {
                var now = +new Date();

                if (now - lastCall < delay) {
                    return;
                }

                lastCall = now;
                fn.apply(this, arguments);
            }
        }

        function closeNav() {
