    <h2>Why Buy New</h2>

    <p>Here are some tips, advice and research on why buying new might be the right option for you:</p>

    <div class="clearfix top">

        <div class="split2l">

            <h3>Move straight in</h3>

            <p class="bottom">No upward chain means you can move in as soon as your new home is completed instead of waiting around for long chains of properties to complete. With no immediate work required, take your time deciding and decorate to your own style.</p>

            <h3>Save money</h3>

            <p class="bottom">The immediate need to redecorate, repair and improve a second hand home can run to several thousand pounds. Save precious time and money and add that to developer deals and fuel savings and you're better off in a new home.</p>

            <h3>As you like it</h3>

            <p class="bottom">Many new homebuilders offer schemes for you to personalize your home prior to moving in with choices of kitchen styles, appliances, carpets, tiles and even garden landscaping.</p>

            <h3>Government help for buyers</h3>

            <p class="bottom">Help to Buy offers equity loans for new homes on selected developments, along with the First Time Buyers Initiative these and a variety of other schemes are designed to help new home buyers. <a href="/help-to-buy/">More details</a>.</p>

        </div>

        <div class="split2r">

            <h3>Developer deals</h3>

            <p class="bottom">Most new homebuilders offer a range of incentives that the second hand market simply can't. From Deposit Paid to Mortgage Paid for a term, Part Exchange to cash back or free new furnishings - new homes can provide a great deal.</p>

            <h3>Peace of mind</h3>

            <p class="bottom">All new homes should carry a 10 year warranty providing buyers protection either via The National House Building Council (NHBC), or other leading insurance companies for certain problems resulting from any construction issues.</p>

            <h3>Energy efficiency</h3>

            <p class="bottom">With new stringent regulations eco-friendly new homes are on average 60% more energy efficient than a period home. These measures mean great savings on home fuel costs for you.</p>

            <h3>Better security</h3>
            <p class="bottom">High building standards require new homes to adhere to strict regulations for electrical insulation and fire safety along with modern locks on all doors and windows, alarms and even entryphones for some developments.</p>

