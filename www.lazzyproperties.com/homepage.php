            subnavControls = [].slice.call(document.querySelectorAll('.subnav__controls a')),
            zooplaProButtons = [].slice.call(document.querySelectorAll('.subnav--agents .button')),
            accessibleAccountLinks = [].slice.call(document.querySelectorAll('.notifications__link')),
            initialViewPortWidth = window.innerWidth || document.documentElement.clientWindth,
            isTouchEnabled = 'ontouchstart' in document,
            activeClass = 'js-active',
            expandedClass = 'js-expanded',
            noScrollClass = 'js-noscroll',
            tappedClass = 'js-tapped',
            hasOrientationChanged = false;

        [].push.apply(accessibleSubnavItems, subnavControls);
        [].push.apply(accessibleSubnavItems, zooplaProButtons);

        function throttle(fn, delay) {
            if (typeof fn !== 'function') {
                return;
            }

            var lastCall = 0;
            delay = typeof delay === 'number' ? delay : 100;

            return function() {
                var now = +new Date();

                if (now - lastCall < delay) {
                    return;
                }

                lastCall = now;
                fn.apply(this, arguments);
            }
        }

        function closeNav() {
            [].slice.call(document.getElementsByClassName(expandedClass)).forEach(function(el) {
                el.classList.remove(expandedClass);
            });

            updateAccessibilityForSubnav(false, accessibleAccountLinks);

            document.body.classList.remove(noScrollClass);
        }

        function resetNavigation(isSmallScreen) {
            if (isSmallScreen) {
                closeNav();
            }
        }

        // unfortunately IE will still complain about using
        // el.classList.contains('some-class') on SVG elements
        function hasClass(element, cls) {
            return (' ' + element.className + ' ').indexOf(' ' + cls + ' ') > -1;
        }

        // hide subnav for large screens
        function hideSubnavigation() {
            var activeEl = document.getElementsByClassName(activeClass)[0];

            if (activeEl) {
                activeEl.classList.remove(activeClass);
            }
        }

        function expandSubnav(e) {
            updateAccessibilityForSubnav(true, accessibleSubnavItems);
            e.preventDefault();
            e.target.nextElementSibling.classList.add(expandedClass);
        }

        function toggleDisplayOfSubnav(e) {
            if (e.target.offsetTop > 0) {
                e.target.previousElementSibling.classList.toggle(activeClass);
            } else {
                e.target.previousElementSibling.classList.remove(activeClass);
            }
        }

        function addToggleToSubnavItems(isSmallScreen) {
            if (subNavs.length) {
                subNavs.forEach(function(el) {
                    if (!isSmallScreen) {
                        el.addEventListener('mouseenter', toggleDisplayOfSubnav, false);
                        el.addEventListener('mouseleave', toggleDisplayOfSubnav, false);

                    } else {
                        el.removeEventListener('mouseenter', toggleDisplayOfSubnav, false);
                        el.removeEventListener('mouseleave', toggleDisplayOfSubnav, false);
                    }
                });
            }
        }

        function handleTap(e) {
            var thisEl = e.target,
                wasTapped = thisEl.classList.contains(tappedClass);

            if (e.type === 'touchstart' && !wasTapped) {
                e.preventDefault();

            } else if (e.type === 'touchend') {
                if (wasTapped) {
                    thisEl.classList.remove(tappedClass);

                } else {
                    subNavTriggers.forEach(function(el) {
                        if (el.classList.contains(tappedClass)) {
                            el.classList.remove(tappedClass);
                        }
                    });

                    thisEl.classList.add(tappedClass);
                    e.preventDefault();
                }
            }
        }

        function handleTouchEnabledNav(isSmallScreen) {
            if (isTouchEnabled && subNavTriggers.length) {
                subNavTriggers.forEach(function(el) {

                    if (!isSmallScreen) {
                        el.addEventListener('touchstart', handleTap, false);
                        el.addEventListener('touchend', handleTap, false);
                    } else {
                        el.removeEventListener('touchstart', handleTap, false);
                        el.removeEventListener('touchend', handleTap, false);
                    }
                });
            }
        }

        function addAccessToSubnav(isSmallScreen) {
            if (openSubNavToggles.length) {
                openSubNavToggles.forEach(function(el) {

                    if (isSmallScreen) {
                        el.addEventListener('click', expandSubnav, false);

                        if (!el.hasAttribute('title')) {
                            el.setAttribute('title', 'Opens sub navigation');
                        }

                    } else {
                        el.removeEventListener('click', expandSubnav, false);

                        if (el.hasAttribute('title')) {
                            el.removeAttribute('title');
                        }
                    }
                });
            }
        }

        function updateAccessibilityForSubnav(isSmallScreen, items) {
            if (items.length) {
                items.forEach(function(el) {
                    if (isSmallScreen) {
                        el.removeAttribute('tabindex');
                    } else {
                        el.setAttribute('tabindex', '-1');
                    }
                });
            }
        }

        function updateNavForScreenSize(isInitialLoad) {
            var viewportWidth = window.innerWidth || document.documentElement.clientWindth,
                smallBreakpoint = 960,
                isSmall = viewportWidth <= smallBreakpoint;

            hideSubnavigation();

            if (isInitialLoad === true || hasOrientationChanged || initialViewPortWidth !== viewportWidth) {
                addAccessToSubnav(isSmall);
                resetNavigation(isSmall);
                addToggleToSubnavItems(isSmall);
                updateAccessibilityForSubnav(false, accessibleSubnavItems);
                handleTouchEnabledNav(isSmall);
            }
        }

        // Prepare subnavigation and accessibility features
        // if the screen size matches criteria

        updateNavForScreenSize(true);

        // Add event listeners

        window.addEventListener('resize', throttle(updateNavForScreenSize));

